    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog (null, "Simpan Data?","Konfirmasi Dialog",JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok==0){
            String sql = "insert into data_karyawan values (?,?,?,?)";
                try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txt_idKaryawan.getText());
                stat.setString(2, txt_nama.getText());
                stat.setString(3, txt_kontak.getText());
                stat.setString(4, txt_alamat.getText());       

                stat.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "Data akan disimpan");
                kosong();
                txt_idKaryawan.requestFocus();
                tabelDataKaryawan();
                }
                catch (SQLException e){
                JOptionPane.showMessageDialog(rootPane, "Data gagal disimpan"+e);
                tabelDataKaryawan();
                }
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog (null, "Ubah Data?","Konfirmasi Dialog",JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok==0){
            try{
                String sql = "update data_karyawan set "
                + "id_karyawan=?, "
                + "nama_karyawan=?, "
                + "kontak=?, "
                + "alamat=? "
                + "where id_karyawan=?";

                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txt_idKaryawan.getText());
                stat.setString(2, txt_nama.getText());
                stat.setString(3, txt_kontak.getText());
                stat.setString(4, txt_alamat.getText());
                stat.setString(5, txt_idKaryawan.getText());

                stat.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "Data Akan Diubah");
                kosong();
                txt_idKaryawan.requestFocus();
                }
            catch (SQLException e){
                JOptionPane.showMessageDialog(rootPane, "Data Gagal Diubah"+e);
                }
            tabelDataKaryawan();
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

