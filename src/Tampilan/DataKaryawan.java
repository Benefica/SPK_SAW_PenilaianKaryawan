    private Connection setKoneksi() {
        try {
                Class.forName("com.mysql.jdbc.Driver");
                conn=DriverManager.getConnection("jdbc:mysql://localhost/penilaian_karyawan","root","");
                stm=conn.createStatement();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,"Koneksi Gagal :" +e);
            }
           return conn; 
    }
    
    private void kosong() {
        txt_idKaryawan.setText("");
        txt_nama.setText("");
        txt_kontak.setText("");
        txt_alamat.setText("");       
    }
    
    private void tabelDataKaryawan() {
        Object header[]={"ID KARYAWAN","NAMA KARYAWAN","KONTAK","ALAMAT"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        tb_dataKaryawan.setModel(data);
        setKoneksi();
        String sql="select * from data_karyawan";
        try{
            ResultSet rs=stm.executeQuery(sql);
                while (rs.next())
                {                
                    String kolom1=rs.getString("id_karyawan");
                    String kolom2=rs.getString("nama_karyawan");
                    String kolom3=rs.getString("kontak");
                    String kolom4=rs.getString("alamat");

                    String kolom[]={kolom1,kolom2,kolom3,kolom4};
                    data.addRow(kolom);
                }
        }catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "data gagal dipanggil"+e);
        }
    }
    
    public void autoIDKaryawan() {
        try {
            ResultSet rs = stm.executeQuery("SELECT * FROM data_karyawan ORDER BY id_karyawan DESC");

            if (rs.next()) {
                int lastID = Integer.parseInt(rs.getString("id_karyawan").substring(3));
                int newID = lastID + 1;
                String AN = String.format("%03d", newID);
                txt_idKaryawan.setText("KRY" + AN);
            } else {
                txt_idKaryawan.setText("KRY001");
            }
        } catch (Exception e) {
            txt_idKaryawan.setText("KRY001");
        }
    }
